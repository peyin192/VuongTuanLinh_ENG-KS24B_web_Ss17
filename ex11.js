// sử dụng hàm Math.abs()
console.log(Math.abs(-5.5));
console.log(Math.abs(10));
 // trả về giá trị tuyệt đối

// sử dụng hàm Math.ceil()
console.log(Math.ceil(4.4));
console.log(Math.ceil(5.8)); 
// làm tròn đến số nguyên gần nhất

// sử dụng hàm Math.floor()
console.log(Math.floor(5.7)); 
console.log(Math.floor(6.1)); 
// làm tròn xuống số nguyên gần nhất

// sử dụng hàm Math.round()
console.log(Math.round(10.1)); 
console.log(Math.round(20.6));
// làm tròn lên số nguyên gần nhất
